 
package variablediclear;
 
public class VariableDiclear {

  
    public static void main(String[] args) {
        
        
         boolean BOOL= true;
         
         char CHAR= 'A';
         
         int INT=1234;
         
         float FLOAT =123.12f;
         
         double DOUB =12345.0987;
         
         
         System .out.println("BOOL IS   "+ BOOL);
         
         System .out.println(" INT IS    " +INT);
         
         System .out.println("CHAR IS    "+ CHAR);
         
         System .out.println("FLOAT IS    " +FLOAT);
         
         System .out.println(" DOUBLE IS   " +DOUB);
         
    }
    
}
